# Arithmetic Operators
print("Arithmetic Operators")
a = 10
b = 5
print(a + b)  
print(a - b) 
print(a * b)  
print(a / b)  
print(a // b) #  (floor division)
print(a % b)  #  (modulus)
print(a ** b) #  (power)


#Assignment Operators
x = 5
x += 2  # x = x + 2 = 7
x *= 3  # x = x * 3 = 21

# Comparison Operators/relational operators
print("Comparison Operators")
a = 10
b = 20
print(a == b)  # False
print(a != b)  # True
print(a < b)   # True
print(a >= b)  # False

#Logical Operators
print("Logical Operators")
x = 5
print(x > 2 and x < 10)  # True ( cantion1 and cantion2 dono right ho )
print(x > 2 or x > 10)   # True (any cantion me 1 right ho)
print(not(x > 2))        # False

